function displayHouse(house, rowHouse) {



    const column = document.createElement("div");
    column.classList.add("col");
    rowHouse.appendChild(column);



    const houseCard = document.createElement("div");
    houseCard.classList.add("card");
    houseCard.style = "width: 18rem;";
    column.appendChild(houseCard);


    const cardImg = document.createElement("img");
    cardImg.classList.add("card-img-top");
    cardImg.src = house.image;
    houseCard.appendChild(cardImg);

    const cardBody = document.createElement("div");
    cardBody.classList.add("card-body");
    houseCard.appendChild(cardBody);

    const cardHeading = document.createElement("h5");
    cardHeading.classList.add("card-title");
    const cardText = document.createTextNode(house.name);
    cardHeading.appendChild(cardText);
    cardBody.appendChild(cardHeading);

    const descrip = document.createElement("p");
    const descripText = document.createTextNode(house.description);
    descrip.appendChild(descripText);
    cardBody.appendChild(descrip);


}

const housesContainer = document.querySelector("#houses-container");

const row = document.createElement("div");
row.classList.add("row");

housesContainer.appendChild(row);



for (let i = 0; i < houses.length; i++) {
    displayHouse(houses[i], row);
}
